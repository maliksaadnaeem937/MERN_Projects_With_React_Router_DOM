import mongoose from "mongoose";

const mySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      trim: true,
    },
  }
);

const userModel = mongoose.model("registereduser", mySchema);

export default userModel;
